#list
fruits = ["apple", "banana", "cherry"]
print(fruits)
#list can have a different type of data in the array
fruits1 = ["apple",65,"test"]
print(fruits1)
#Regular indexing in a list
fruits2 = ["apple","banana","cherry"]
print(fruits2[0])
#Replacing value in index
fruits[0] = "orange"
print(fruits)
#negetive indexing
print(fruits[-1])
fruits[-1] = "mango"
print(fruits)
## list `append` function"
fruits.append("apple")
print(fruits)